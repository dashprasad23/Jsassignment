package com.TY.beanObject.bin;

public class Student {
	private int id;
	private String name;
	private int rollno;
	public void setID(int id)
	{
		this.id=id;
		
	}
	public int getId()
	{
		return id;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public String getName()
	{
		return name;
	}
	public void setRollNo(int rollno)
	{
		this.rollno=rollno;
	}
	public int getRollNo()
	{
		return rollno;
	}

}
